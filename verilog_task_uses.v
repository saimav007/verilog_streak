$display, $monitor, $strobe	Prints the specified message or value to the console or waveform viewer during simulation.
$fopen, $fscanf, $fclose	File operations to open, scan lines and close files
$random	Generates a random value within the specified range.
$stop, $finish	Simulation termination commands to stop/exit execution
$readmemb, $readmemh	Reads memory contents from a file into a Verilog array.
